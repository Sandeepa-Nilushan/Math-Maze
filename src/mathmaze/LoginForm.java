    private void jLabel_loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_loginButtonMouseClicked

        PreparedStatement st;
        ResultSet rs;

        //get username and password
        String username = jTextField_user.getText();
        String password = String.valueOf(jPasswordField_Pass.getPassword());

        //create a select query to check if the username and the password exist in the currnt database
        String query = "SELECT * FROM `users` WHERE `username` = ? AND `password` = ?";

        try {
            st = db_mathmaze.getConnection().prepareStatement(query);

            st.setString(1, username);
            st.setString(2, password);

            rs = st.executeQuery();

            if (rs.next()) {

                // show the game mode form
                showMessageDialogWithTimer(username);
                
                GameModeForm form = new GameModeForm(username);
                form.setVisible(true);
                form.pack();
                form.setLocationRelativeTo(null);

                //close the current LoginForm
                this.dispose();

            } else {
                //send error message to the user
                JOptionPane.showMessageDialog(null, "Invalid Username / Password !!! Please enter valid details to login...", "Login Error!", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jLabel_loginButtonMouseClicked

